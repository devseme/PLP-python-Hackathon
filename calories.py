# calculates the number of calories that result from the fat, using the following formula: 
# Calories from fats fat grams * 9 
# Calories from carbs carb grams * 4 

# Calories from fats fat grams
fatsGrams = int(input ("enter the number of fats grams:"))

fatsCalories = fatsGrams * 9
print (fatsCalories, "kcal")

# Calories from carbs carb grams * 4 
carbsGrams = int(input ("enter the number of carbs grams:"))
carbsCalories =  carbsGrams * 4
print (carbsCalories, "kcal")